def extract_m2c_prototypes(m2c_output):
    """Extract function prototypes that m2c placed at the top of its output.

    m2c emits lines like:
        ? FuncName(type, type, ...);  /* extern */
    Returns dict: func_name -> prototype_string.
    """
    protos = {}
    if not m2c_output:
        return protos
    for line in m2c_output.splitlines():
        # Match lines that look like a prototype with /* extern */ comment
        m = re.match(r'(.+?\b(\w+)\s*\([^)]*\)\s*;)\s*/\*\s*extern\s*\*/', line.strip())
        if m:
            protos[m.group(2)] = m.group(1).strip()
    return protos


