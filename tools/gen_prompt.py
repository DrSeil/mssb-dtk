COMPILER_PATTERNS = """\
### Pointer Globals vs Array Globals
Two distinct assembly patterns appear for globals — they require different C code:

- `lis rN, SYM@ha` + `addi rDst, rN, SYM@l` → `rDst = &SYM` (symbol IS the data — array or struct in-place)
- `lis rN, SYM@ha` + `lwz  rDst, SYM@l(rN)` → `rDst = *SYM` (symbol CONTAINS a pointer — dereference it)

If the assembly uses `lwz` with `@l`, the symbol is a **pointer global**: its first field (offset 0x0)
is a pointer to the actual data array. You must go through that field:
```c
// WRONG (addi pattern — symbol is the array):
elem = &sym[arg0];

// CORRECT (lwz pattern — symbol holds a pointer at offset 0):
elem = &sym.ptr_field[arg0];
```
The `⚠ POINTER-GLOBAL` warning in Referenced Symbols flags this case. When you see it,
field offsets listed belong to the **pointed-to element type**, not to the declared type of `sym`.

### Global Address Re-loading
The compiler re-loads the base address of a global variable (using `lis`/`addi`) even if
already in a register. Creating a local pointer (`Type* p = &g_Global;`) causes the
compiler to hold the address in one register, which mismatches the original. Use direct
global access (`g_Global.field = ...;`) in distinct logical blocks instead.

### Structure Assignments and Update-Form Instructions
The binary uses "load/store with update" instructions (`lfsu`, `stfsu`). These are
generated by structure-level assignments (`dst.pos = src.pos;`), NOT by individual field
copies (`dst.x = src.x; dst.z = src.z;`).

### Register Steering ("Anchoring") — Last Resort Only
Registers `r31`/`r30` are typically used for major object base pointers. Define a local
pointer at the exact line where assembly first allocates the base register. Defining it
at the top of the function causes the compiler to use a different register.
**Do NOT reach for register steering on the first attempt.** Write the simplest correct C
first. Only use `register` keywords or carefully ordered variable initializations if a
clean version fails to match after at least one build+diff cycle.

### ROData Ordering
Float constants are stored in `.rodata` in the order first encountered in C code. If
your code references `1.0f` before `0.0f` but the original did the reverse, rodata
labels will mismatch. Order your constant references to match the assembly.

### Loop Simplicity: for vs do-while
CW often optimizes `for (i = 0; i < N; i++)` into a bottom-test loop (`cmpwi`/`blt` at
the bottom of the loop body) when it can prove the loop body executes at least once (e.g.
when N is a compile-time constant > 0). **Start with a simple `for` loop even when the
assembly shows a bottom-branching `do { } while` pattern.** Only switch to an explicit
`do { } while` if the `for` loop fails to match after a clean attempt.
Similarly, don't manually replicate the assembly's stride arithmetic with separate loop
variables — use clean array indexing (`arr[i]`) and let the compiler generate the strides.

### Casting and Sign Extension
Use explicit casts to match `extsh` (sign-extend halfword) and `extsb` (sign-extend byte).
Ensure `lwz` vs `lhz` vs `lbz` matches your struct field types (u32 vs u16 vs u8).

### Float Expression Scheduling (fmuls / fmsubs)
For float expressions of the form `A*B - C*D`, CW generates:
```
fmuls dest, C, D          ; compute subtracted term first
fmsubs dest, A, B, dest   ; A*B - (C*D)
```
CW interleaves `lfs` loads naturally with the float ops when expressions are written
directly. **Do NOT pre-load struct fields into local float variables before the
expression.** Write inline:
```c
dest->x = vec1->y * vec2->z - vec1->z * vec2->y;  // CORRECT
```
NOT:
```c
f32 y1 = vec1->y, z1 = vec1->z, y2 = vec2->y, z2 = vec2->z;
dest->x = y1 * z2 - z1 * y2;  // WRONG — pre-loading disrupts scheduling
```
This applies to all float arithmetic: cross products, dot products, linear
interpolations, etc. Start with clean direct expressions; only introduce a temp
if the diff shows a specific register issue that the temp would fix.

### C89 Variable Declarations
The compiler (Metrowerks CodeWarrior) is extremely strict about C89 rules.
CRITICAL: You MUST declare ALL variables at the very BEGINNING of a block (`{ ... }`), before any other code or statements (like `if`, `memcpy`, or assignments).
Failure to do this will cause an "expression syntax error".
INCORRECT:
```c
void func() {
    int a;
    a = 5;
    int b; // SYNTAX ERROR: declaration after assignment
}
```
CORRECT:
```c
void func() {
    int a;
    int b; // Both at top
    a = 5;
}
```

### M2C_ERROR Means Missing Parameter
When m2c outputs `M2C_ERROR(/* Read from unset register $rX */)`, it means register rX
held an input value that m2c did not account for — i.e., the function has an additional
parameter passed in rX. The calling convention passes parameters in r3, r4, r5, r6, ...
in order. If you see `$r4` unset, the function has at least 2 parameters. If you see
`$r5`, it has at least 3, etc. Add the missing parameter(s) to the function signature.
Example: `M2C_ERROR(/* Read from unset register $r4 */)` → add `int arg1` as second param.

### Non-Void Return Type Detection
If the assembly contains `mr rSAVED, r3` immediately after a `bl` call (saving the
return value of the called function to a callee-saved register like r31 or r30), AND
`mr r3, rSAVED` appears just before `blr` (restoring it as the return value), then the
OUTER function is non-void — it returns the result of the inner call. Check the callee's
full prototype to determine the return type, and declare the outer function accordingly.
Example pattern:
```
bl  sndFXStartEx      ; returns SND_VOICEID
...
mr  r31, r3           ; save return value
bl  sndFXCtrl
...
mr  r3, r31           ; return the saved value
blr
```
→ outer function returns `SND_VOICEID`.

### Integer Offset vs. Pointer for Cross-Call Index Variables
When the same array index (e.g. `arg0 * 2`) must survive across a function call AND
the array's base address is a known global, use an INTEGER offset variable, NOT a
pointer variable:
```c
// CORRECT: saves only the int in r31; reloads global base each time
int off = arg0 * 2;
u8 a = ((u8*)&globalTable + off)[-0x2a2];
sndFXStartEx(..., a, ...);
u8 b = ((u8*)&globalTable + off)[-0x2a1];
```
```c
// WRONG: saves the full pointer in r31; prevents the double lis/addi reload
u8* ptr = (u8*)&globalTable + arg0 * 2;
u8 a = ptr[-0x2a2];
sndFXStartEx(..., a, ...);
u8 b = ptr[-0x2a1];
```
The "wrong" version produces an extra register move before the first access and only
one lis/addi pair instead of two. Use the "correct" version whenever the assembly
shows two separate lis/addi sequences for the same global."""

