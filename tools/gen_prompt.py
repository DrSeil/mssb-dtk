    # --- Assembly analysis hints ---
    sig_hints = analyze_function_signature(asm_text)
    div_hints = detect_integer_division(asm_text)
    indexed_types = detect_indexed_access(asm_text, set(syms))

    hint_lines = []

    # Parameter type hints
    if sig_hints['params']:
        for idx, ptype in sig_hints['params']:
            reg = f"r{idx + 3}"
            hint_lines.append(
                f"- **Parameter {idx} ({reg})** narrows to `{ptype}` at function entry "
                f"— declare as `{ptype}` in the function signature."
            )

    # Return type hint
    if sig_hints['return_type'] and sig_hints['return_type'] != '?':
        hint_lines.append(
            f"- **Return type**: assembly narrows/extends `r3` before `blr` → return type is `{sig_hints['return_type']}`."
        )
    elif sig_hints['return_from_callee']:
        hint_lines.append(
            f"- **Return type**: function saves r3 from `{sig_hints['return_from_callee']}` and returns it — "
            f"check `{sig_hints['return_from_callee']}`'s return type and use it here."
        )

    # Integer division hints
    for _divisor, desc in div_hints:
        hint_lines.append(f"- **Integer division**: {desc}.")

    # Signature mismatch warning
    if (sig_hints['params'] or sig_hints['return_type']) and header_content:
        # Find the declared signature in the header
        decl_pat = re.compile(
            rf'(\w[\w\s\*]*?)\s+\b{re.escape(func_name)}\s*\(([^)]*)\)\s*;'
        )
        hm = decl_pat.search(header_content)
        if hm:
            declared_ret = hm.group(1).strip()
            declared_params = hm.group(2).strip()
            inferred_ret = sig_hints['return_type']
            mismatches = []
            if inferred_ret and inferred_ret != '?' and declared_ret == 'void':
                mismatches.append(
                    f"return type is declared `void` but assembly implies `{inferred_ret}`"
                )
            if sig_hints['params'] and (declared_params == 'void' or declared_params == ''):
                mismatches.append(
                    f"function is declared with no parameters but assembly implies "
                    + ", ".join(f"arg{i}:{t}" for i, t in sig_hints['params'])
                )
            if mismatches:
                hint_lines.append(
                    f"\n> **WARNING — signature mismatch**: "
                    + "; ".join(mismatches) + ". "
                    f"Update the signature in **both** the header (`include/...`) and source file."
                )

    if hint_lines:
        sections.append(f"\n## Assembly Analysis Hints\n")
        sections.append("\n".join(hint_lines))

    # Dependencies
    m2c_protos = extract_m2c_prototypes(m2c_output)
    if deps:
        sections.append(f"\n## Dependencies (bl calls)\n")
        for dep in deps:
            sig = find_signature(dep)
            if sig:
                sections.append(f"- `{dep}`: `{sig}`")
            elif dep in m2c_protos:
                sections.append(f"- `{dep}` (not in headers; m2c suggests: `{m2c_protos[dep]}`)")
            else:
                sections.append(f"- `{dep}` (signature not found in headers)")

    # Referenced symbols (annotated with size/type/stride hints + indexed access types)
    if syms:
        sym_section = annotate_referenced_symbols(
            syms, asm_text, module=info.get("module"), indexed_types=indexed_types
        )
        sections.append(f"\n{sym_section}")

    # Current feedback
    if feedback and "MATCH!" not in feedback:
        sections.append(f"\n## Current Diff (compiled vs target)\n")
        sections.append(f"```\n{feedback.strip()}\n```")

    # m2c approximate decomp
    if m2c_output:
        sections.append(f"\n## Approximate Decompilation (from m2c)\n")
        sections.append(f"```c\n{m2c_output.strip()}\n```")

        # Callout: M2C_ERROR means the function signature is wrong
        errors = re.findall(r'M2C_ERROR\(/\* Read from unset register \$(r\d+) \*/\)', m2c_output)
        if errors:
            regs = ", ".join(sorted(set(errors)))
            sections.append(
                f"\n> **WARNING — wrong function signature**: m2c found unset register(s) `{regs}`. "
                f"This means the function has parameter(s) that are not declared in the current signature. "
                f"The calling convention passes arguments in r3, r4, r5, ... in order — "
                f"add the missing parameter(s) to the function signature in **both** the header and source file "
                f"before implementing the function body."
            )

    # Ghidra decompilation (with address annotations)
    if ghidra_output:
        annotated_ghidra = annotate_ghidra_addresses(ghidra_output, module=info.get("module"))
        sections.append(f"\n## Ghidra Decompilation (from in_game.c)\n")
        sections.append(f"```c\n{annotated_ghidra.strip()}\n```")

    # Header file
    if header_content:
        rel = os.path.relpath(header_path, ROOT_DIR)
        sections.append(f"\n## Header: `{rel}`\n")
        sections.append(f"```c\n{header_content.strip()}\n```")

    # Source file
    if src_content:
        rel = os.path.relpath(src_path, ROOT_DIR)
        sections.append(f"\n## Source File: `{rel}`\n")
        sections.append(f"```c\n{src_content.strip()}\n```")

    # Instructions
    sections.append(f"\n## Instructions\n")
    sections.append(INSTRUCTIONS_TEMPLATE.format(func=func_name))

    return "\n".join(sections)


